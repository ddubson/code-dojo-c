int main() {
  short int a; // 2 bytes (16-bit)
  short a2; // same as 'a'
  short signed int a3; // same as 'a'
  short unsigned int a4; // 0 to 65,535

  int b; // 2 or 4 bytes (16-bit, mostly 32-bit)
  unsigned int b2;

  long int c; // 4 bytes (32-bit)
  long c2; // same as 'c'

  float d; // 4 bytes
  double e; // 8 bytes
  long double f; // 10 bytes

  char ch = 'A'; // char's single quotes
  char ch2 = 70; // ascii value for 'F'


}
